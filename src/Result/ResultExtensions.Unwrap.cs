using System;
using System.Threading.Tasks;

namespace Wrap;

public static partial class ResultExtensions
{
	/// <summary>
	/// Unwrap the value contained in <paramref name="this"/>.
	/// </summary>
	/// <remarks>
	/// <para>
	/// Provides access to the value wrapped by a <see cref="Result{T}"/> object.
	/// </para>
	/// <para>
	/// You need to provide a default value via <paramref name="ifNone"/> in case
	/// <paramref name="this"/> is <see cref="Fail"/>.
	/// </para>
	/// </remarks>
	/// <typeparam name="T">Ok value type.</typeparam>
	/// <param name="this">Result object.</param>
	/// <param name="ifNone">Function to generate a value if <paramref name="this"/> is <see cref="Fail"/>.</param>
	/// <returns>The value of <paramref name="this"/> or generated by <paramref name="ifNone"/>.</returns>
	public static T Unwrap<T>(this Result<T> @this, Func<FailValue, T> ifNone) =>
		R.Match(@this,
			err: ifNone,
			ok: x => x
		);

	/// <inheritdoc cref="Unwrap{T}(Result{T}, Func{FailValue,T})"/>
	public static Task<T> UnwrapAsync<T>(this Result<T> @this, Func<FailValue, Task<T>> ifNone) =>
		R.MatchAsync(@this,
			err: ifNone,
			ok: x => x
		);

	/// <inheritdoc cref="Unwrap{T}(Result{T}, Func{FailValue,T})"/>
	public static Task<T> UnwrapAsync<T>(this Task<Result<T>> @this, Func<FailValue, T> ifNone) =>
		R.MatchAsync(@this,
			err: ifNone,
			ok: x => Task.FromResult(x)
		);

	/// <inheritdoc cref="Unwrap{T}(Result{T}, Func{FailValue,T})"/>
	public static Task<T> UnwrapAsync<T>(this Task<Result<T>> @this, Func<FailValue, Task<T>> ifNone) =>
		R.MatchAsync(@this,
			err: ifNone,
			ok: x => Task.FromResult(x)
		);
}
